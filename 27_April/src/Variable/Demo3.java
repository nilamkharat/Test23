package Variable;

public class Demo3 
{
	public static void main(String[] args)
	{
		//variable declaration
		
		String studentname;             //datatype variablename
		int studentrollno;            
		char studentgrade;
		float studentpercentage;
		
		//variable initialization
		
		studentname="Rohit";                    //variablename=variabledata
		studentrollno=20;
		studentgrade='A';
		studentpercentage=80.23f;
		
		//Usage
		
		System.out.println(studentname);               //Rohit
		System.out.println(studentrollno);             //20
		System.out.println(studentgrade);                //A
		System.out.println(studentpercentage);            //80.23
		
		
		
		
		
	}
	

}
